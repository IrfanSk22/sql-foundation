-- FORIEGN KEY
A foreign key in SQL is a column or a set of columns in a table that refers to the 
primary key or a unique key of another table. It establishes a link between two tables 
by enforcing referential integrity, ensuring that the values in the foreign key columns 
of one table correspond to the values in the primary key or unique key columns of another table.


-- COMPOSITE KEY
A composite key in SQL is a key that consists of two or more columns in a table, used together 
to uniquely identify a record. It is defined by applying the PRIMARY KEY constraint to multiple 
columns, ensuring that the combination of values in these columns is unique for each record. Composite 
keys are often used when no single column can uniquely identify a record.

