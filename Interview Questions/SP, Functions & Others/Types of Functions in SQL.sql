/*

1. **Aggregate Functions:**
   - Perform calculations on a set of values and return a single result.
   - Examples: `SUM()`, `COUNT()`, `AVG()`, `MIN()`, `MAX()`.

2. **Scalar Functions:**
   - Operate on a single value and produce a single result.
   - Examples: `LEN()`, `UPPER()`, `LOWER()`, `CONCAT()`.

3. **Window Functions:**
   - Perform calculations across a set of table rows related to the current row.
   - Examples: `ROW_NUMBER()`, `RANK()`, `SUM() OVER()`.

4. **String Functions:**
   - Manipulate and process string values.
   - Examples: `SUBSTRING()`, `CHARINDEX()`, `LEN()`.

5. **Date and Time Functions:**
   - Operate on date and time values.
   - Examples: `GETDATE()`, `DATEPART()`, `DATEDIFF()`.

6. **Conversion Functions:**
   - Convert data from one type to another.
   - Examples: `CAST()`, `CONVERT()`.

7. **Mathematical Functions:**
   - Perform mathematical operations.
   - Examples: `ROUND()`, `ABS()`, `CEILING()`.

These functions are commonly used in SQL queries to perform various operations on data.

