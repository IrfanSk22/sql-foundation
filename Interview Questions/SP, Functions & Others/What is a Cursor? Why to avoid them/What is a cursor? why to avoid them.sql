/*

- A cursor in SQL is like a pointer that helps you go through one row at a time in the results of a query. 

- Think of it as a tool to help you work with each row individually, allowing you to perform specific actions 
or operations on each row, such as retrieving data or updating values. 

- Cursors can be either automatically created by the system (implicit cursors) or explicitly declared and 
managed by the programmer (explicit cursors). 

- They're handy when you need to do something with each row of a result set, but they should be used carefully 
because they can impact performance.

